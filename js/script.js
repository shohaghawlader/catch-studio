const galleryGrid = $('#galleryGrid');
const filterButtons = $$('.filter-btn');
const loadMoreBtn = $('#loadMoreBtn');
let currentFilter = 'all';
let visibleCount = 12;
let activeItems = [];
let currentIndex = 0;

function getFilteredItems() {
  return currentFilter === 'all' ? galleryItems : galleryItems.filter(item => item.category === currentFilter);
}

function renderGallery(reset = true) {
  if (!galleryGrid) return;
  if (reset) visibleCount = 12;
  activeItems = getFilteredItems();
  const itemsToShow = activeItems.slice(0, visibleCount);
  galleryGrid.innerHTML = itemsToShow.map((item, index) => `
    <button class="gallery-item" data-index="${index}" data-caption="${item.caption}">
      <img src="${item.thumb}" alt="${item.caption}" loading="lazy">
    </button>
  `).join('');
  $$('.gallery-item', galleryGrid).forEach(button => {
    button.addEventListener('click', () => openLightbox(Number(button.dataset.index)));
  });
  loadMoreBtn.style.display = visibleCount >= activeItems.length ? 'none' : 'inline-flex';
}

filterButtons.forEach(btn => btn.addEventListener('click', () => {
  filterButtons.forEach(b => b.classList.remove('active'));
  btn.classList.add('active');
  currentFilter = btn.dataset.filter;
  renderGallery(true);
}));

loadMoreBtn?.addEventListener('click', () => {
  visibleCount += 12;
  renderGallery(false);
});
renderGallery(true);

const lightbox = $('#lightbox');
const lightboxImg = $('#lightboxImg');
const lightboxCaption = $('#lightboxCaption');
function openLightbox(index) {
  currentIndex = index;
  const item = activeItems[currentIndex];
  if (!item) return;
  lightboxImg.src = item.src;
  lightboxImg.alt = item.caption;
  lightboxCaption.textContent = item.caption;
  lightbox.classList.add('active');
  lightbox.setAttribute('aria-hidden', 'false');
  document.body.style.overflow = 'hidden';
}
function closeLightbox() {
  lightbox.classList.remove('active');
  lightbox.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = '';
}
function moveLightbox(direction) {
  currentIndex = (currentIndex + direction + activeItems.length) % activeItems.length;
  openLightbox(currentIndex);
}
$('#lightboxClose')?.addEventListener('click', closeLightbox);
$('#lightboxPrev')?.addEventListener('click', () => moveLightbox(-1));
$('#lightboxNext')?.addEventListener('click', () => moveLightbox(1));
lightbox?.addEventListener('click', (e) => { if (e.target === lightbox) closeLightbox(); });
document.addEventListener('keydown', (e) => {
  if (!lightbox?.classList.contains('active')) return;
  if (e.key === 'Escape') closeLightbox();
  if (e.key === 'ArrowLeft') moveLightbox(-1);
  if (e.key === 'ArrowRight') moveLightbox(1);
});

$$('.faq-item').forEach(item => item.addEventListener('click', () => {
  const wasActive = item.classList.contains('active');
  $$('.faq-item').forEach(i => i.classList.remove('active'));
  if (!wasActive) item.classList.add('active');
}));

const hoursInput = $('#hoursInput');
const addonInputs = $$('.addonInput');
const totalAmount = $('#totalAmount');
function formatBDT(amount) {
  return '৳' + amount.toLocaleString('en-US');
}
function updateEstimate() {
  const hours = Math.max(4, Number(hoursInput?.value || 4));
  if (hoursInput && Number(hoursInput.value) < 4) hoursInput.value = 4;
  const addons = addonInputs.reduce((sum, input) => sum + (input.checked ? Number(input.value) : 0), 0);
  const total = hours * 700 + addons;
  if (totalAmount) totalAmount.textContent = formatBDT(total);
}
hoursInput?.addEventListener('input', updateEstimate);
addonInputs.forEach(input => input.addEventListener('change', updateEstimate));
updateEstimate();

